#include "3-calc.h"

/**
 * op_add - adding
 * @a: param a
 * @b: param b
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - operation sub
 * @a: param a
 * @b: param b
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply
 * @a: param a
 * @b: param b
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - operation div
 * @a: param a
 * @b: param b
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod divide
 * @a: param a
 * @b: param b
 * Return: ont
 */

int op_mod(int a, int b)
{
	return (a % b);
}
